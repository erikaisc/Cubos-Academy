//toUpperCase coloca todas as letras de uma determinada string em formato maiúsculo. O método toLowerCase() é o posto e coloca todas as letras de uma string em minúsculas.

const texto = "Eu amo quejio, amo muito.";

const gritando = texto.toUpperCase();
console.log(gritando);

const texto1 = "Eu amo quejio, amo muito.";

const sussurando = texto1.toLowerCase();
console.log(sussurando);